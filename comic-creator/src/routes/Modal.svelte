<script lang="ts">
  import Button from "./Button.svelte";
  import { createComicImage } from "../api";

  export let showModal: boolean;
  export let imagePrompt: any;

  let dialog: HTMLDialogElement;
  $: if (dialog && showModal) dialog.showModal();
  let resp: any = "";

  const handleSubmit = async () => {
    resp = await createComicImage(imagePrompt);
    const a = document.createElement("a");
    a.href = resp;
    a.download = "generated_image.png";
    document.body.appendChild(a);
    a.click();
    window.URL.revokeObjectURL(resp);
  };
</script>

<!-- svelte-ignore a11y-click-events-have-key-events a11y-no-noninteractive-element-interactions -->
<dialog
  bind:this={dialog}
  on:close={() => (showModal = false)}
  on:click|self={() => dialog.close()}
>
  <!-- svelte-ignore a11y-no-static-element-interactions -->
  <div on:click|stopPropagation>
    <slot name="header" />
    <hr />
    <slot />
    <hr />
    <!-- svelte-ignore a11y-autofocus -->
    <div id="buttonContainer">
      <Button classList={["danger", "sm"]} on:click={() => dialog.close()}>
        Close
      </Button>
      <Button classList={["primary", "sm"]} on:click={handleSubmit}>
        Submit
      </Button>
    </div>
  </div>
</dialog>

